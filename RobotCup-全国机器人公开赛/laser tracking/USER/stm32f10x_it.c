/*******************************************************************************
* Function Name  : TIM2_IRQHandler
* Description    : This function handles TIM2 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void TIM2_IRQHandler(void)	  //10ms中断一次
{
	unsigned int i;
	 
	if (TIM_GetITStatus(TIM2, TIM_IT_Update) != RESET)  TIM_ClearITPendingBit(TIM2, TIM_IT_Update);
  	/*用户代码*/
	TIM2_num++;

	/* 编码器关联 */  //编码器脉冲采样周期10ms
	i = TIM_GetCounter(TIM1);  //i = Code_CNT;
	if(i>100) i = 0;
	TIM_SetCounter(TIM1, 0);   // 清零计数器CNT  Code_CNT = 0;
	iEncoder       = i;
	lEncoderTotal += iEncoder;
	if( iEncoder > iEncoderMax )  iEncoderMax = iEncoder;
}  

