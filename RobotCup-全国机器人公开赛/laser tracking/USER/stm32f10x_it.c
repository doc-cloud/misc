/*******************************************************************************
* Function Name  : EXTI4_IRQHandler
* Description    : This function handles External interrupt Line 4 request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void EXTI4_IRQHandler(void)
{
	u8 res=0;
    u8 OK=0; 
    u8 RODATA=0;   		 
	while(1)
    {        
        if(RDATA)//有高脉冲出现
        {
            res=Pulse_Width_Check();//获得此次高脉冲宽度       
            if(res==250)break;//非有用信号
            if(res>=200&&res<250)OK=1; //获得前导位(4.5ms)
            else if(res>=85&&res<200)  //按键次数加一(2ms)
            {  							    		 
                Remote_Rdy=1;//接受到数据
                Remote_Cnt++;//按键次数增加
				switch(Remote_Process())
				{
					case 162: 
						Sys_Soft_Reset();				
						break;
					case 48:					
						break;
					case 24:
						break;
					case 122:
						BEEP_ON();
						break;
					case 16:
						BEEP_OFF();
						break;
					case 34:
						
						break;
					case 56:
						
						break;
					case 224:
						
						break;
					case 168:
					
						break;
					case 144:
						
						break;
					case 104:
					
						break;
					case 152:
					
						break;
					case 176:
						
						break;
					case 66:
						
						break;
					case 82:
					
						break;	    
				}
                break;
            }
            else if(res>=50&&res<85)RODATA=1;//1.5ms
            else if(res>=10&&res<50)RODATA=0;//500us
            if(OK)
            {
                Remote_Odr<<=1;
                Remote_Odr+=RODATA; 
                Remote_Cnt=0; //按键次数清零
            }   
        }			 						 
    } 	 	    
	EXTI->PR=1<<4;      //清除中断标志位
}

