void lcd_cmd_wr(uchar cmdcode, uchar left)//向LCD1284写指令		
{
  LCD_RS =0;
  LCD_E =1;
  if(left==1)	//left＝1，则对右半屏进行读写
  {
        LCD_CSA =1;
	LCD_CSB =0;
  }
  else			//left＝0，则对左半屏进行读写
  {
	LCD_CSA =0;
        LCD_CSB =1;
  }
  LCD_DATA =cmdcode;
  LCD_E  =0;
  delay(10,1);
}
void lcd_data_wr(uchar ldata, uchar left)//向LCD1284写8位数据		
{
  LCD_RS =1;
  LCD_E =1;
  if(left==1)   //left＝1，则对右半屏进行读写
  {
    LCD_CSA =1;
	LCD_CSB =0;
  }
  else		    //left＝0，则对左半屏进行读写
  {
	LCD_CSA =0;
    LCD_CSB =1;
  }
  LCD_DATA =ldata;
  LCD_E =0;
  delay(10,1);
}
void lcd_show_number(uchar x_row, uchar y_rank, uint number_lcd_data, uchar right_left)//向LCD1284写数字、字母或字符（8*16点阵）
{ //x_row:设置显示数字、字母或字符的行地址；
  //y_rank:设置显示数字、字母或字符的列地址；
  //number_lcd_data：显示数字、字母或字符在data_number[]数组中的序号 
  //right_left：左右半屏选择
  uchar show_i;
  lcd_cmd_wr(0x40+y_rank, right_left);  //设置显示数字或字符上半部分的列地址
  lcd_cmd_wr(0xb8+x_row, right_left);   //设置显示数字或字符上半部分的行地址
  number_lcd_data=(number_lcd_data)*16;
  for(show_i=0; show_i<8; show_i++)
  {   
    lcd_data_wr(data_number[number_lcd_data], right_left);
    number_lcd_data=(number_lcd_data)+1;
  } 
  lcd_cmd_wr(0x40+y_rank, right_left);    //设置显示数字或字符下半部分的列地址
  lcd_cmd_wr(0xb8+x_row+1, right_left);   //设置显示数字或字符下半部分的行地址
  for(show_i=0; show_i<8; show_i++)
  {   
    lcd_data_wr(data_number[number_lcd_data], right_left);
    number_lcd_data=(number_lcd_data)+1;
  } 
}
